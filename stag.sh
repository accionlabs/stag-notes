main() {
    local command=$1
    shift
    
    case $command in
        init)
            cmd_init "$@"
            ;;
        add-shared)
            cmd_add_shared "$@"
            ;;
        add-project)
            cmd_add_project "$@"
            ;;
        sync)
            cmd_sync "$@"
            ;;
        status)
            cmd_status "$@"
            ;;
        contribute)
            cmd_contribute "$@"
            ;;
        serve)
            cmd_serve "$@"
            ;;
        create-project)
            cmd_create_project "$@"
            ;;
        help|-h|--help)
            show_help
            ;;
        version|-v|--version)
            show_version
            ;;
        *)
            if [ -z "$command" ]; then
                show_help
            else
                log_error "Unknown command: $command"
                echo ""
                show_help
                exit 1
            fi
            ;;
    esac
}

